"""
Conversão de tipos, coerção
type convertion, typecasting, coercion
é o ato de converter um tipo em outro
tipos imutaveis e primitívos:
str, int, float, bool
"""

# Classe int
print('1', type('1'))
print(int('1'), type(int('1')))
print(int('1') + 1)

# Classe float

print(float('1') + 1)

# Classe bool

print(bool(''))
print(bool('1'))
