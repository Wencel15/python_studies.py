# função print imprime os argumentos correspondetes  
# \r\n faz com que o computador quebre a linha autolaticamente = CRLF

print(12) #Imprime 1 Argumento

print(12, 34) #Imprime 2 ou mais argumentos utilizando ,

#Separador utilizado para separar os argumentos

print(12, 34, 1011, sep='Sep') 

#Utilizando o end=
# podemos retirar a ou colocar a quebra de linha

print(12, 34, 1011, sep='Sep', end='##')
print(12, 34, 1011, sep='Sep', end='\n##')  #\n para quebras de linha, antes ou depois
print(1,2,3) #exemplificando o end.
